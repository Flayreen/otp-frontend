import ServerSelector from '@/components/selectServer/SelectServer.vue';
import baseheader from '@/components/base/Header.vue';
import basefooter from '@/components/base/Footer.vue';
import axios from 'axios';

export default {
  components: { baseheader, basefooter, ServerSelector },
  data() {
    return {
      steamID: null,
      activeTab: 'Вбивства',
      notification: '',
      response: {
        kills: [],
        longest_kill: [],
        playtime: []
      },
      currentPort: localStorage.getItem('selectedServerPort') || 'cherno_3pp', 
      intervalId: null,
      currentServerLink: '', 
    }
  },
  computed: {
    baseURL() {
      return `https://otpdayz.store:3000/${this.currentPort}`;
    }
  },
  methods: {
    async logout() {
      if (localStorage.getItem('steamID')) {
        localStorage.removeItem('steamID')
        location.href = 'https://otpdayz.store'
      }
    },
    handleLinkChange(link) {
      this.currentServerLink = link;
    },
    fetchLeaderboard(url) {
      axios.get(`${url}/getLeaderboard`).then((res) => {
        this.response = res.data;
      });
    },
    checkForPortChange() {
      const newPort = localStorage.getItem('selectedServerPort') || 'cherno_3pp';
      if (newPort !== this.currentPort) {
        this.currentPort = newPort;
        this.fetchLeaderboard(this.baseURL);
      }
    },
  },
  created() {
    if (localStorage.getItem('steamID') != null) {
      this.steamID = localStorage.getItem('steamID');
    }

    this.fetchLeaderboard(this.baseURL);

    this.intervalId = setInterval(this.checkForPortChange, 1000);
  },
  beforeDestroy() {
    if (this.intervalId) {
      clearInterval(this.intervalId);
    }
  }
}
</script>


